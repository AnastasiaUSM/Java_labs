//Водителей беспокоит расход топлива их автомобилей. Один водитель ведет учет нескольких
//поездок записывая пройденные мили и использованные галлоны для каждого бака. Разработайте
//приложение на языке Java, которое будет вводить пройденные мили и использованные галлоны
//(оба значения в виде целых чисел)для каждой поездки. Программа должна вычислять и отображать
//расход топлива(мили на галлон), полученный для каждой поездки, и выводить общий средний расход
//топлива для всех поездок на данный момент. Все вычисления среднего значения должны выдавать
//результаты с плавающей запятой. Используйте класс Scanner и цикл с управлением с помощью
// контрольного значения для получения данных от пользователя.
import java.util.Scanner;

public class GasMileage {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);

        int miles;
        int gallons;  
        int totalMiles = 0;
        int totalGallons = 0;

        while (true) {
            // Ввод данных о поездке
            System.out.print("Введите пройденные мили (-1 для завершения): ");
            miles = input.nextInt();

            // Проверка на окончание ввода
            if (miles == -1) {
                break;  // выход из цикла, если введено -1
            }

            System.out.print("Введите использованные галлоны: ");
            gallons = input.nextInt();

            // Вычисление расхода топлива для текущей поездки
            double tripMileage = (double) miles / gallons;
            System.out.printf("Миль на галлон для этой поездки: %.2f%n", tripMileage);

            // Обновление общего количества миль и галлонов
            totalMiles += miles;
            totalGallons += gallons;

            // Вычисление общего расхода топлива для всех поездок
            double totalMileage = (double) totalMiles / totalGallons;
            System.out.printf("Общий миль на галлон до текущего момента: %.2f%n%n", totalMileage);
        }

        System.out.println("Программа завершена.");
        input.close();
    }
}
