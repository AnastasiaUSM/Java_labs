//Процесс нахождения наибольшего значения часто используется в компьютерных приложениях.
//Например, программа, которая определяет победителя конкурса продаж, может вводить
//количество проданных единиц каждым продавцом. Продавец, который продал больше всего единиц,
//выигрывает конкурс. Напишите псевдокод программы, а затем Java-приложение, которое вводит
//серию из 10 целых чисел, определяет и выводит наибольшее целое число. Ваша программа должна
//использовать по крайней мере следующие три переменные:
//a) counter: счетчик для подсчета до 10 (т.е. чтобы отслеживать количество введённых
//чисел и определить, когда все 10 чисел будут обработаны).
//b) number: целое число, недавно введенное пользователем.
//c) largest: наибольшее число, найденное на данный момент.
import java.util.Scanner;

public class LargestNumberFinder {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // Инициализация переменных
        int counter = 0;
        int largest = Integer.MIN_VALUE; // Наименьшее возможное значение
        int number;

        // Цикл для ввода 10 чисел
        while (counter < 10) {
            System.out.print("Введите число #" + (counter + 1) + ": ");
            number = scanner.nextInt(); // Считываем число

            // Обновляем значение largest, если введенное число больше
            if (number > largest) {
                largest = number;
            }

            counter++;
        }

        // Вывод наибольшего числа
        System.out.println("Наибольшее число: " + largest);
    }
}
